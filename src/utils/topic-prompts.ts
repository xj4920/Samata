/**
 * 根据主题获取专属 prompt 配置
 */
export function getTopicPromptConfig(topic?: string): TopicPromptConfig {
  if (!topic) {
    return TOPIC_PROMPTS['通用'];
  }

  // 精确匹配
  if (TOPIC_PROMPTS[topic]) {
    return TOPIC_PROMPTS[topic];
  }

  // 关键词匹配
  for (const config of Object.values(TOPIC_PROMPTS)) {
    if (config.keywords.some(keyword => topic.includes(keyword))) {
      return config;
    }
  }

  // 默认返回通用配置
  return TOPIC_PROMPTS['通用'];
}

/**
 * 生成主题专属的提取 prompt
 */
export function generateTopicPrompt(
  conversationText: string,
  topic: string | undefined,
  limit: number
): string {
  const config = getTopicPromptConfig(topic);

  const examplesText = config.examples
    .map(
      (ex, i) => `
示例 ${i + 1}：
Q: ${ex.question}
A: ${ex.answer}`
    )
    .join('\n');

  return `你是一个业务知识提取专家。请从以下企业微信聊天记录中提取关于"${config.topic}"主题的高质量 Q&A 对。

${config.extractionStrategy}

**必须提取的内容类型：**
${config.mustExtractTypes.join('\n')}

**可用标签列表：**
[${QA_TAGS.join(', ')}]
（请根据 Q&A 内容，从上述标签列表中严格选择 1-3 个最贴切的标签）

**严格排除的内容：**
❌ 特定时间的安排（如"五一后上线"、"下周三测试"）
❌ 特定客户的个性化需求（如"贵司需要定制XX功能"）
❌ 临时性的状态通知（如"系统正在维护"、"已修复"）
❌ 人员协调和会议安排（如"明天开会讨论"）
❌ 缺乏技术或业务细节的简单确认（如"好的"、"收到"）

**提取标准：**
${config.extractionFocus.join('\n')}
- 最多提取 ${limit} 个最有价值的 Q&A 对

**提取示例：**
${examplesText}

聊天记录格式说明：
每条消息格式为 [时间] 发送人: 消息内容

聊天记录：
${conversationText}

请以 JSON 数组格式返回，每个元素包含：
- question: 问题内容（提炼为通用问题，去除客户特定信息）
- answer: 答案内容（提炼为通用答案，综合多条消息，去除时间和客户特定信息）
- tags: 标签列表（**必须**从"可用标签列表"中严格选择，返回 JSON 字符串数组，如 ["FIX协议", "拒单分析"]）
- time: 问题提出的时间（格式：YYYY-MM-DD HH:MM:SS）
- questioner: 提问人（**必须**从消息中的"发送人"字段提取真实姓名，如果无法确定则留空字符串""，**禁止**填"未知"）
- answerer: 回答人（**必须**从消息中的"发送人"字段提取真实姓名，如果无法确定则留空字符串""，**禁止**填"未知"）
- context: 业务场景说明

只返回 JSON 数组，不要用 markdown 代码块包裹，不要其他说明文字。如果没有符合标准的 Q&A，返回空数组 []。
答案应简明扼要，每个答案控制在 300 字以内，避免过度展开。`;
}
